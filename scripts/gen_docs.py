#!/usr/bin/env python3
"""
scripts/gen_docs.py — regenerate all help documentation from skill manifests.

Single source of truth = the `help:` block in each src/agents/*/skill.yaml
(plus the manifest fields the registry already parses: input file_types,
output suffix/extension, requires.native_binaries / .llm).

Renders, from that one source:
  * docs/user-guide/<skill_dir>.md   — one end-user guide per UI skill
  * docs/USER-GUIDE.html             — one self-contained, bundleable HTML guide
  * docs/dev/skills-reference.md      — developer reference (UI + 6 internal steps)

Usage:
    python scripts/gen_docs.py            # write all outputs
    python scripts/gen_docs.py --check    # exit 1 if anything would change (CI)

Editing help text: edit the skill's skill.yaml `help:` block and re-run this.
See docs/dev/help-block-schema.md and docs/dev/editing-help.md.
"""
from __future__ import annotations

import argparse
import html
import sys
from pathlib import Path

import yaml

# --- Locate project + make `agents` importable (mirrors the app's path setup).
ROOT = Path(__file__).resolve().parent.parent
SRC = ROOT / "src"
AGENTS = SRC / "agents"
DOCS = ROOT / "docs"
sys.path.insert(0, str(SRC))

from agents import registry  # noqa: E402  (after sys.path tweak)

GENERATED_BANNER = (
    "<!-- GENERATED by scripts/gen_docs.py from skill.yaml help: blocks. "
    "Do not edit by hand — edit the manifest and re-run. -->"
)
MD_BANNER = (
    "<!-- GENERATED by scripts/gen_docs.py — do not edit by hand. "
    "Edit the skill's skill.yaml `help:` block and re-run `python scripts/gen_docs.py`. -->"
)


# ---------------------------------------------------------------------------
# Extract a uniform view per skill from manifest + help block.
# ---------------------------------------------------------------------------

def _output_pattern(skill: registry.SkillInfo) -> str:
    folder = (skill.help.output_folder if skill.help else "Data/outputs/").rstrip("/")
    if skill.output.type == "directory":
        return f"{folder}/YYYY-MM-DD-HHMMSS-{skill.output.suffix}/"
    return f"{folder}/YYYY-MM-DD-HHMMSS-<input>-{skill.output.suffix}{skill.output.extension}"


def _runs_offline(skill: registry.SkillInfo) -> bool:
    return not skill.requires.llm


def skill_view(skill: registry.SkillInfo) -> dict:
    """Flatten a SkillInfo + help block into a render-ready dict."""
    h = skill.help
    help_inputs = {hi.name: hi for hi in (h.inputs if h else ())}

    inputs = []
    for inp in skill.inputs:
        hi = help_inputs.get(inp.name)
        formats = ", ".join(inp.file_types) if inp.file_types else (
            "Text" if inp.type in ("text", "select") else "File / folder"
        )
        inputs.append({
            "label": inp.label,
            "required": inp.required,
            "formats": (hi.accepts if hi and hi.accepts else formats),
            "tooltip": hi.tooltip if hi else "",
            "gotchas": hi.gotchas if hi else "",
        })

    return {
        "name": skill.name,
        "display_name": skill.display_name,
        "dir": skill.manifest_path.parent.name,
        "category": skill.category,
        "mode": skill.mode,
        "offline": _runs_offline(skill),
        "native": list(skill.requires.native_binaries),
        "external": list(skill.requires.external_tools),
        "description": " ".join(skill.description.split()),
        "overview": (h.overview if h else ""),
        "when_to_use": (h.when_to_use if h else ""),
        "inputs": inputs,
        "steps": list(h.steps) if h else [],
        "output_folder": (h.output_folder if h else "Data/outputs/"),
        "output_pattern": _output_pattern(skill),
        "output_files": [{"name": f.name, "tooltip": f.tooltip}
                         for f in (h.output_files if h else ())],
        "tips": (h.tips if h else ""),
        "troubleshooting": [{"problem": t.problem, "fix": t.fix}
                            for t in (h.troubleshooting if h else ())],
        "has_help": bool(h and not h.is_empty()),
    }


def internal_steps() -> list[dict]:
    """Scan manifests the registry skips (no display_name) = internal steps."""
    steps = []
    ui_dirs = {s.manifest_path.parent.name for s in registry.discover()}
    for manifest in sorted(AGENTS.glob("*/skill.yaml")):
        if manifest.parent.name in ui_dirs:
            continue
        raw = yaml.safe_load(manifest.read_text(encoding="utf-8")) or {}
        if not isinstance(raw, dict) or "display_name" in raw:
            continue
        req = raw.get("requires") or {}
        steps.append({
            "dir": manifest.parent.name,
            "category": raw.get("category", "general"),
            "version": raw.get("version", "0.0.0"),
            "description": " ".join((raw.get("description") or "").split()),
            "llm": bool(req.get("llm", False)),
        })
    return steps


# ---------------------------------------------------------------------------
# Markdown rendering (per-skill end-user guide).
# ---------------------------------------------------------------------------

def render_skill_md(v: dict) -> str:
    badge = "🔌 offline (no LLM)" if v["offline"] else "🤖 needs an LLM endpoint"
    deps = ""
    if v["native"] or v["external"]:
        alldeps = ", ".join(v["native"] + v["external"])
        deps = f" · requires: {alldeps}"
    lines = [
        MD_BANNER,
        f"# {v['display_name']} — Usage Guide",
        "",
        f"*Mode: {v['mode']} · {badge}{deps}*",
        "",
        v["overview"] or v["description"],
        "",
    ]
    if v["when_to_use"]:
        lines += ["## When to use it", "", v["when_to_use"], ""]
    lines += ["## Inputs", ""]
    for inp in v["inputs"]:
        req = "required" if inp["required"] else "optional"
        lines.append(f"- **{inp['label']}** ({req}) — accepts: {inp['formats']}")
        if inp["tooltip"]:
            lines.append(f"  - {inp['tooltip']}")
        if inp["gotchas"]:
            lines.append(f"  - ⚠️ {inp['gotchas']}")
    lines += ["", "## How to run", ""]
    if v["steps"]:
        for i, s in enumerate(v["steps"], 1):
            lines.append(f"{i}. {s}")
    else:
        lines.append("1. Provide the input(s) above and click **Run**.")
    lines += [
        "",
        "## Output",
        "",
        f"Output is written to the **`{v['output_folder']}`** folder, named:",
        "",
        f"```\n{v['output_pattern']}\n```",
        "",
    ]
    if v["output_files"]:
        lines.append("Files produced:")
        lines.append("")
        for f in v["output_files"]:
            lines.append(f"- **`{f['name']}`** — {f['tooltip']}" if f["tooltip"]
                         else f"- **`{f['name']}`**")
        lines.append("")
    if v["tips"]:
        lines += ["## Tips", "", v["tips"], ""]
    if v["troubleshooting"]:
        lines += ["## Troubleshooting", "", "| If… | Then… |", "|------|-------|"]
        for t in v["troubleshooting"]:
            prob = t["problem"].replace("|", "\\|")
            fix = t["fix"].replace("|", "\\|")
            lines.append(f"| {prob} | {fix} |")
        lines.append("")
    return "\n".join(lines).rstrip() + "\n"


# ---------------------------------------------------------------------------
# HTML rendering (one self-contained, bundleable guide).
# ---------------------------------------------------------------------------

def _h(s: str) -> str:
    return html.escape(s or "")


def render_skill_html(v: dict) -> str:
    badge = "offline · no LLM" if v["offline"] else "needs LLM endpoint"
    badge_cls = "ok" if v["offline"] else "warn"
    parts = [f'<section id="{_h(v["dir"])}" class="skill">']
    parts.append(f'<h2>{_h(v["display_name"])} '
                 f'<span class="badge {badge_cls}">{_h(badge)}</span> '
                 f'<span class="badge">{_h(v["mode"])}</span></h2>')
    parts.append(f'<p class="overview">{_h(v["overview"] or v["description"])}</p>')
    if v["when_to_use"]:
        parts.append(f'<p class="whenuse"><b>When to use it.</b> {_h(v["when_to_use"])}</p>')

    parts.append("<h3>Inputs</h3><ul class=\"inputs\">")
    for inp in v["inputs"]:
        req = "required" if inp["required"] else "optional"
        tip = ""
        if inp["tooltip"] or inp["gotchas"]:
            tip_txt = inp["tooltip"]
            if inp["gotchas"]:
                tip_txt += (" ⚠️ " + inp["gotchas"]) if tip_txt else ("⚠️ " + inp["gotchas"])
            tip = f'<span class="pa-tip" data-tip="{_h(tip_txt)}">ℹ️</span>'
        parts.append(
            f'<li><b>{_h(inp["label"])}</b> <em>({req})</em> {tip}'
            f'<br><span class="fmt">accepts: {_h(inp["formats"])}</span></li>'
        )
    parts.append("</ul>")

    parts.append("<h3>How to run</h3><ol>")
    steps = v["steps"] or ["Provide the input(s) above and click Run."]
    for s in steps:
        parts.append(f"<li>{_h(s)}</li>")
    parts.append("</ol>")

    parts.append("<h3>Output</h3>")
    parts.append(f'<p>Written to <code>{_h(v["output_folder"])}</code>:</p>')
    parts.append(f'<pre>{_h(v["output_pattern"])}</pre>')
    if v["output_files"]:
        parts.append('<ul class="outputs">')
        for f in v["output_files"]:
            tip = (f'<span class="pa-tip" data-tip="{_h(f["tooltip"])}">ℹ️</span>'
                   if f["tooltip"] else "")
            desc = f' — {_h(f["tooltip"])}' if f["tooltip"] else ""
            parts.append(f'<li><code>{_h(f["name"])}</code>{tip}{desc}</li>')
        parts.append("</ul>")
    if v["tips"]:
        parts.append(f'<h3>Tips</h3><p>{_h(v["tips"])}</p>')
    if v["troubleshooting"]:
        parts.append('<h3>Troubleshooting</h3><table class="tbl"><thead><tr>'
                     '<th>If…</th><th>Then…</th></tr></thead><tbody>')
        for t in v["troubleshooting"]:
            parts.append(f'<tr><td>{_h(t["problem"])}</td><td>{_h(t["fix"])}</td></tr>')
        parts.append("</tbody></table>")
    parts.append("</section>")
    return "\n".join(parts)


