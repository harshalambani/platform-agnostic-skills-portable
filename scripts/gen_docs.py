# ---------------------------------------------------------------------------
# Dev reference (UI skills + internal steps).
# ---------------------------------------------------------------------------

def render_dev_reference(views: list[dict], internal: list[dict]) -> str:
    lines = [MD_BANNER, "# Skills Reference (developer)", "",
             "Auto-generated inventory of every skill. UI skills appear as tabs; "
             "internal steps run only inside the GnuCash Import pipeline.", "",
             "## UI skills", "",
             "| Skill | Dir | Mode | LLM | Inputs | Output | Native deps |",
             "|-------|-----|------|-----|--------|--------|-------------|"]
    for v in views:
        ins = "; ".join(f"{i['label']} ({i['formats']})" for i in v["inputs"]) or "—"
        deps = ", ".join(v["native"] + v["external"]) or "—"
        llm = "no" if v["offline"] else "yes"
        lines.append(
            f"| {v['display_name']} | `{v['dir']}` | {v['mode']} | {llm} | "
            f"{ins} | `{v['output_pattern'].split('/')[-1]}` | {deps} |"
        )
    lines += ["", "## Internal pipeline steps", "",
              "Not registered as UI tabs (no `display_name`). Used programmatically "
              "by `skill_gnucash_pipeline`.", "",
              "| Step (dir) | Category | LLM | What it does |",
              "|------------|----------|-----|--------------|"]
    for s in internal:
        lines.append(f"| `{s['dir']}` | {s['category']} | "
                     f"{'yes' if s['llm'] else 'no'} | {s['description']} |")
    lines.append("")
    return "\n".join(lines)


# ---------------------------------------------------------------------------
# Driver.
# ---------------------------------------------------------------------------

def build() -> dict[Path, str]:
    skills = registry.discover(refresh=True)
    views = [skill_view(s) for s in skills]
    internal = internal_steps()
    # NB: no generation date here — stamping date.today() made the committed
    # HTML "stale" every day after commit, breaking the docs-freshness check
    # non-deterministically. Keep the note deterministic (counts only).
    version_note = (f"Generated from skill manifests · "
                    f"{len(views)} UI skills, {len(internal)} internal steps.")

    out: dict[Path, str] = {}
    for v in views:
        out[DOCS / "user-guide" / f"{v['dir']}.md"] = render_skill_md(v)
    out[DOCS / "USER-GUIDE.html"] = render_html(views, version_note)
    out[DOCS / "dev" / "skills-reference.md"] = render_dev_reference(views, internal)
    return out


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--check", action="store_true",
                    help="Exit 1 if any output is stale (CI mode); write nothing.")
    args = ap.parse_args()

    outputs = build()
    stale = []
    for path, content in outputs.items():
        existing = path.read_text(encoding="utf-8") if path.exists() else None
        if existing != content:
            stale.append(path)
        if not args.check:
            path.parent.mkdir(parents=True, exist_ok=True)
            path.write_text(content, encoding="utf-8")

    if args.check:
        if stale:
            print("Stale docs (run scripts/gen_docs.py):")
            for p in stale:
                print(f"  - {p.relative_to(ROOT)}")
            return 1
        print("Docs up to date.")
        return 0

    print(f"Wrote {len(outputs)} file(s):")
    for p in outputs:
        flag = "  (changed)" if p in stale else ""
        print(f"  - {p.relative_to(ROOT)}{flag}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
